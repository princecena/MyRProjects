# Principles:
# 1. Show Comparisons - Eg: Placing air cleaner in the home increased the symptom free days of asthma

# 2. Show causality,mechanism,explaination,systematic structure
  #Eg: Symptom free days occured due to the reduce in the fine particulate matter

# 3. Show multivariate data - Real World is multivariate.Multivariate is more than 2 variables
  
# 4. Integration of evidence
#   - Completely integrate words, numbers, images, diagrams
#   - Data graphics should make use of many modes of data presentation
#   - Don't let the tool drive the analysis

# 5. Describe and document the evidence with appropriate labels, scales, sources etc.
#    - A data graphic should tell a complete story that is credible

# 6. Content is King
#    - Analytical presentations ultimately stand  or fall depending on the quality, relevance,
#      and integrity of the content.

# Edwart Tufte(2006). Beautiful Evidence.www.edwarttufte.com
