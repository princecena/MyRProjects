# For Loop
######################################################################################################
for (i in 1:10) {
  print(i)
}

x <- c("a","b","c","d")
for (i in seq_along(x)) {
  print(x[i])
}

for(letter in x) {
  print(letter)
}

for(i in 1:4) print(x[i])

x <- matrix(1:6,2,3)
for(i in seq_len(nrow(x))) {
  for(j in seq_len(nrow(x))) {
    print(x[i,j])
  }
}

######################################################################################################
# While Loop
######################################################################################################
count <- 0
while(count < 10) {
  print(count)
  count <- count + 1
}

######################################################################################################
# Repeat Loop
######################################################################################################
#Repeat initiates an infinite loop. The only way to exit a repeat loop is to call break
x0 <- 1
tol <- le-8

repeat {
  x1 <- computeEstimate()
  
  if(abs(x1 - x0) < tol) {
    break
  } else {
    x0 <- x1
  }
}

